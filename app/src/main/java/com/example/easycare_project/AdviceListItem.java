package com.example.easycare_project;

public class AdviceListItem {
    private String head;
    private String desc;

    public AdviceListItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
